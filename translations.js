function changeLanguage(lang) {
    localStorage.setItem('selectedLanguage', lang);
    
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            if (key === "hero-btn" || key === "eq-modal-btn") {
                element.innerHTML = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    const modalTitle = document.getElementById('m-title');
    const modalDesc = document.getElementById('m-desc');
    if(modalTitle && modalTitle.getAttribute('data-modal-title')) {
        const titleKey = modalTitle.getAttribute('data-modal-title');
        const descKey = modalDesc.getAttribute('data-modal-desc');
        if(translations[lang][titleKey]) modalTitle.textContent = translations[lang][titleKey];
        if(translations[lang][descKey]) modalDesc.textContent = translations[lang][descKey];
    }

    document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active-lang'));
    const activeBtn = document.getElementById(`btn-${lang}`);
    if (activeBtn) activeBtn.classList.add('active-lang');
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLanguage') || 'ka';
    changeLanguage(savedLang);
});